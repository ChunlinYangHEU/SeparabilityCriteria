% Wely算子基底
function [g] = operator_weyl(d)
    g = cell(1, d^2 -1);
    if d == 2
        g{1} = [1, 0;
                0, 1];
        g{2} = [0, 1;
                1, 0];
        g{3} = [0, -1i;
                1i, 0];
        g{4} = [1, 0;
                0, -1];
    elseif d == 3
        w = exp(1i * 2 * pi / 3);
        g{1} = [1, 0, 0;
                0, 1, 0;
                0, 0, 1];
        g{2} = [0, 1, 0; 
                0, 0, 1; 
                1, 0, 0];
        g{3} = [0, 0, 1; 
                1, 0, 0; 
                0, 1, 0];
        g{4} = [1, 0, 0; 
                0, w, 0; 
                0, 0, w ^ 2];
        g{5} = [0, 1, 0; 
                0, 0, w; 
                w ^ 2, 0, 0];
        g{6} = [0, 0, 1; 
                w, 0, 0; 
                0, w ^ 2, 0];
        g{7} = [1, 0, 0; 
                0, w ^ 2, 0; 
                0, 0, w];
        g{8} = [0, 1, 0; 
                0, 0, w ^ 2; 
                w, 0, 0];
        g{9} = [0, 0, 1; 
                w ^ 2, 0, 0; 
                0, w, 0];
    end
end